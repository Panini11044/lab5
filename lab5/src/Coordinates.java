public class Coordinates {
    private long x; //Максимальное значение поля: 624
    private int y; //Максимальное значение поля: 192
    public Coordinates(long x, int y){
        this. x = x;
        this.y = y;
    }

    public long getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}